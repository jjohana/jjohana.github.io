const stems = [
  (title: string) => `Which statement best describes ${title} for Series 3 study?`,
  (title: string) => `A QCM about ${title} is mainly testing which skill?`,
  (title: string) => `Which exam reminder is most accurate for ${title}?`
];

function q(spec: GapSpec, index: number): Question {
  const correct = [spec.concept, spec.application, spec.reminder][index];
  const choices = [correct, ...spec.distractors].map((text, choiceIndex) => ({
    id: String.fromCharCode(97 + choiceIndex),
    text,
    isCorrect: choiceIndex === 0,
    rationale:
      choiceIndex === 0
        ? `Correct. ${spec.explanation}`
        : `Incorrect. This distractor misses the specific rule or calculation: ${spec.explanation}`
  }));

  return {
    id: `${spec.idPrefix}-${String(index + 1).padStart(3, "0")}`,
    sectionId: spec.sectionId,
    topicId: spec.topicId,
    subtopicId: spec.subtopicId,
    difficulty: spec.difficulty,
    questionType: "multiple_choice",
    stem: stems[index](spec.title),
    choices,
    explanation: spec.explanation,
    sourceType: "user-authored",
    active: true,
    regulatoryFocus: spec.regulatoryFocus,
    concept: spec.title,
    qualityStatus: "verified",
    issueTypes: [],
    qualityNotes: "Original minimum-coverage question authored to bring the subtopic to at least three verified QCMs.",
    verifiedAt: createdAt,
    verifiedBy: "Codex minimum-coverage audit",
    createdAt,
    updatedAt: createdAt
  };
}

